// פונקציה להחלפת שפה
export let currentLanguage = 'he';

export function switchLanguage() {
  currentLanguage = currentLanguage === 'he' ? 'en' : 'he';
  updatePageText();
}


export function updatePageText() {
  const elements = document.body.querySelectorAll('*');
  elements.forEach((element) => {
    if (element.children.length === 0 && element.innerText.trim() !== '') {
      const originalText = element.innerText.trim();
      if (translations[currentLanguage][originalText]) {
        element.innerText = translations[currentLanguage][originalText];
      }
    }
  });
}
